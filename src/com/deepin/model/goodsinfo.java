package com.deepin.model;


public class goodsinfo {
	
	private String goodsId;
	
	private String goodsName;
	
	private int goodsNum;
	
	private String goodsInPrice;
	
	private double goodsOutPrice;
	
	private double goodsDiscount;
	
	private String goodsRemark;
	
	private String enterTime;
	
	public String getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(String goodsId) {
		this.goodsId = goodsId;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public int getGoodsNum() {
		return goodsNum;
	}
	public void setGoodsNum(int goodsNum) {
		this.goodsNum = goodsNum;
	}
	public String getGoodsInPrice() {
		return goodsInPrice;
	}
	public void setGoodsInPrice(String goodsInPrice) {
		this.goodsInPrice = goodsInPrice;
	}
	public double getGoodsOutPrice() {
		return goodsOutPrice;
	}
	public void setGoodsOutPrice(double goodsOutPrice) {
		this.goodsOutPrice = goodsOutPrice;
	}
	public double getGoodsDiscount() {
		return goodsDiscount;
	}
	public void setGoodsDiscount(double goodsDiscount) {
		this.goodsDiscount = goodsDiscount;
	}
	public String getGoodsRemark() {
		return goodsRemark;
	}
	public void setGoodsRemark(String goodsRemark) {
		this.goodsRemark = goodsRemark;
	}
	public String getEnterTime() {
		return enterTime;
	}
	public void setEnterTime(String enterTime) {
		this.enterTime = enterTime;
	}
}
